public class Pokemon{
    private int hp;
    private int attack;
    private int defense;
    private int speed;
    
    //public interface is anything that is declared public
    public int getHp(){
        return hp;
    }
    
    public int getAttack(){
        return attack;
    }
    
    public int getDefense(){
        return defense;
    }
    
    public int getSpeed(){
        return speed;
    }
    
    public void setHp(int userHp){
        hp = userHp;
    }
    
    public void setAttack(int userAttack){
        attack = userAttack;
    }
    
    public void setDefense(int userDefense){
        defense = userDefense;
    }
    
    public void setSpeed(int userSpeed){
        speed = userSpeed;
    }
    }
    
    
    
    
